// Funciones auxiliares para filtrar planes
export const getPlansBySegment = (segment) => {
  const segmentData = plansData.segments[segment]
  if (!segmentData) return []
  return segmentData.plans.map((planId) => plansData.plans[planId]).filter(Boolean)
}

export const getPlansByCategory = (category, subcategory = null) => {
  if (subcategory) {
    const categoryData = plansData[category][subcategory]
    if (!categoryData) return []
    return categoryData.plans.map((planId) => plansData.plans[planId]).filter(Boolean)
  }
  return []
}

export const getFeaturedPlans = () => {
  return plansData.ofertasExclusivas
    .filter((oferta) => oferta.featured)
    .map((oferta) => ({
      ...plansData.plans[oferta.planId],
      offerBadge: oferta.badge,
      discount: oferta.discount,
    }))
}

export const getPlanById = (planId) => {
  return plansData.plans[planId] || null
}
